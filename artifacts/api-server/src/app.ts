// Serve os arquivos do code-editor diretamente do servidor da API em produ脙搂脙拢o.
// Isso garante que WebSocket e frontend ficam no MESMO servi脙搂o, sem problemas
// de roteamento de proxy entre servi脙搂os diferentes.
const STATIC_DIR = path.resolve(process.cwd(), "artifacts/code-editor/dist/public");
